package main.java.com.excilys.training.web.controller.dto;

public class CompanyDto {
	int id;
	String name;
	public CompanyDto(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
